package it.univr;

// coppia di due Object
public class Pair3 {
	private final Object first;
	private final Object second;

	public Pair3(Object first, Object second) {
		this.first = first;
		this.second = second;
	}

	public Object getFirst() {
		return first;
	}

	public Object getSecond() {
		return second;
	}
}
